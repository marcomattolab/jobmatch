import { Component, OnInit, OnDestroy } from '@angular/core';
import { HttpErrorResponse, HttpHeaders, HttpResponse } from '@angular/common/http';
import { ActivatedRoute, Router } from '@angular/router';
import { Subscription } from 'rxjs';
import { filter, map } from 'rxjs/operators';
import { JhiEventManager, JhiParseLinks, JhiAlertService, JhiDataUtils } from 'ng-jhipster';

import { ICandidate } from 'app/shared/model/candidate.model';
import { ISkill } from 'app/shared/model/skill.model';
import { SkillType } from 'app/shared/model/skill-tag.model';
import { AccountService } from 'app/core';

import { ITEMS_PER_PAGE } from 'app/shared';
import { CandidateService } from './candidate.service';

@Component( {
    selector: 'jhi-candidate',
    templateUrl: './candidate.component.html'
} )
export class CandidateComponent implements OnInit, OnDestroy {
    currentAccount: any;
    candidates: ICandidate[];
    error: any;
    success: any;
    eventSubscriber: Subscription;
    routeData: any;
    links: any;
    totalItems: any;
    itemsPerPage: any;
    page: any;
    predicate: any;
    previousPage: any;
    reverse: any;

    constructor(
        protected candidateService: CandidateService,
        protected parseLinks: JhiParseLinks,
        protected jhiAlertService: JhiAlertService,
        protected accountService: AccountService,
        protected activatedRoute: ActivatedRoute,
        protected dataUtils: JhiDataUtils,
        protected router: Router,
        protected eventManager: JhiEventManager
    ) {
        this.itemsPerPage = ITEMS_PER_PAGE;
        this.routeData = this.activatedRoute.data.subscribe( data => {
            this.page = data.pagingParams.page;
            this.previousPage = data.pagingParams.page;
            this.reverse = data.pagingParams.ascending;
            this.predicate = data.pagingParams.predicate;
        } );
    }

    loadAll() {
        this.candidateService
            .query( {
                page: this.page - 1,
                size: this.itemsPerPage,
                sort: this.sort()
            } )
            .subscribe(
            ( res: HttpResponse<ICandidate[]> ) => this.paginateCandidates( res.body, res.headers ),
            ( res: HttpErrorResponse ) => this.onError( res.message )
            );
    }

    loadPage( page: number ) {
        if ( page !== this.previousPage ) {
            this.previousPage = page;
            this.transition();
        }
    }

    transition() {
        this.router.navigate( ['/candidate'], {
            queryParams: {
                page: this.page,
                size: this.itemsPerPage,
                sort: this.predicate + ',' + ( this.reverse ? 'asc' : 'desc' )
            }
        } );
        this.loadAll();
    }

    clear() {
        this.page = 0;
        this.router.navigate( [
            '/candidate',
            {
                page: this.page,
                sort: this.predicate + ',' + ( this.reverse ? 'asc' : 'desc' )
            }
        ] );
        this.loadAll();
    }

    ngOnInit() {
        this.loadAll();
        this.accountService.identity().then( account => {
            this.currentAccount = account;
        } );
        this.registerChangeInCandidates();
    }

    ngOnDestroy() {
        this.eventManager.destroy( this.eventSubscriber );
    }

    trackId( index: number, item: ICandidate ) {
        return item.id;
    }

    byteSize( field ) {
        return this.dataUtils.byteSize( field );
    }

    openFile( contentType, field ) {
        return this.dataUtils.openFile( contentType, field );
    }

    registerChangeInCandidates() {
        this.eventSubscriber = this.eventManager.subscribe( 'candidateListModification', response => this.loadAll() );
    }

    sort() {
        const result = [this.predicate + ',' + ( this.reverse ? 'asc' : 'desc' )];
        if ( this.predicate !== 'id' ) {
            result.push( 'id' );
        }
        return result;
    }

    protected paginateCandidates( data: ICandidate[], headers: HttpHeaders ) {
        this.links = this.parseLinks.parse( headers.get( 'link' ) );
        this.totalItems = parseInt( headers.get( 'X-Total-Count' ), 10 );
        this.candidates = data;
    }

    protected onError( errorMessage: string ) {
        this.jhiAlertService.error( errorMessage, null, null );
    }

    filterSkill( skills: ISkill[] ) {
        return skills.filter( s => s.skillTagType !== SkillType.LANGUAGE );
    }
}
